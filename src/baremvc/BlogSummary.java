package baremvc;

public class BlogSummary {
    public final String id;
    public final String title;
    public final String date;
    public final String url;

    public BlogSummary(String id, String title, String date, String url) {
        this.id = id;
        this.title = title;
        this.date = date;
        this.url = url;
    }
    
}
